#include "ft_check.h"

int		ft_is_neg_sign(char *str, int index)
{
	int	i;

	i = 0;
	while (i < index)
	{
		if (str[i] != ' ')
			return (TRUE);
		i++;
	}
	return (i == 0 ? TRUE : FALSE);
}
